
public class Day03_EX1 {

	public static void main(String[] args) {
		// 정수형 변수 num = 3
		int num = 3;
		
		if (num < 5) { // num이 5보다 작으면,
					   // 소괄호 안에 있는 조건문이 만족 O
			System.out.println("num이 5보다 작습니다.");
		} else { // num이 5보다 크거나 같을때,
			     // 소괄호 안에 있는 조건문이 만족하지 X
			System.out.println("num이 5보다 크거나 같습니다.");
		}	
		
		// if else 끝나면 여기 올거야!
	}
}
