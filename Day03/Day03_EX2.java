public class Day03_EX2 {

	public static void main(String[] args) {
		/*
		 * = : 대입 연산자
		 * == : 같다
		 * 
		 * 만약 apple이 10이면, 10입니다를 출력
		 *             10이 아니면, 10이 아닙니다.
		 */
		int apple = 10; // 정수형 변수 apple 에 10을 넣고.
		
		if (apple == 10) {
			System.out.println("10입니다.");
		} else {
			System.out.println("10이 아닙니다.");
		}
		
		System.out.println("두 갈래 길은 만날까요?");
	}
}
